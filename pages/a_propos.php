        La fabrique a été rachetée en 1935 par dix-sept salariés qui l'ont rebaptisée Union Bisontine des Monteurs de Boîtes (U.B.M.B) et a connu une période de croissance.<br<<br>

        Reprise en 1985 par Daniel BECHE, lui-même ancien salarié de l'UBMB, la société s'est adaptée aux nouvelles demandes du marché. Forte de son savoir-faire dans le travail de l'Or, elle s'est ainsi orientée vers la fabrication de bijoux en Or.<br><br>

        Une boutique commercialise les créations de l'atelier et le succès est au rendez-vous. Les bijoux précieux plaisent, et le magasin est rapidement agrandi. Au fil des années et des agrandissements successifs, la bijouterie prend de l'ampleur. Elle se spécialise rapidement dans le bijou événement et compose une collection Mariage incomparable avec plus de 1500 modèles d'alliances. En complément de ses propres collections, elle commercialise également les bijoux des marques qu'elles sélectionne soigneusement.<br><br>

        La bijouterie aujourd'hui, 150m² et un choix de montres exceptionnelAvant-gardiste, Daniel Bêche décide en 2001 de vendre ses bijoux en ligne. Il développe alors un site marchand et devient ainsi l'un des pionniers de la bijouterie en ligne avec le site ubbijoux.com.<br><br>

        Devant le succès de l'aventure, il lance un site marchand dédié aux alliances : boutique-alliance.com<br><br>

        En recherche constante d'innovation et désireux de revenir à ses premières activités, Daniel Bêche ouvre en 2012 une troisième boutique en ligne spécialisée. C'est la naissance de 1001-montres.fr.<br><br>

        Aujourd'hui, les collections de bijoux précieux, d'alliances et de montres sont présentées au sein de la bijouterie UB Bijoux
